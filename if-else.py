# Функция для расчета стоимости билета
def calculate_ticket_price(age, show_time):
    # Определение базовой стоимости билета
    if age < 3:
        return 0  # Бесплатно для детей (до 3 лет)
    elif 3 <= age <= 12:
        base_price = 10  # Для детей (от 3 до 12 лет)
    else:
        base_price = 15  # Для взрослых (старше 12 лет)

    # Применение скидки для сеансов до 12:00
    if show_time <= 12:
        return base_price * 0.8  # Скидка 20%
    else:
        return base_price  # Без скидки

# Ввод возраста и времени сеанса
age = int(input("Введите возраст посетителя: "))
show_time = float(input("Введите время сеанса: "))

# Расчет стоимости билета
ticket_price = calculate_ticket_price(age, show_time)

# Вывод результата
print(f"Стоимость билета: ${ticket_price:.2f}")






